package ex_01_Java_Basic.Arrays;

public class _2D_Array {
    public static void main(String[] args) {

        int [] [] array_2d = new int[3][3];
        array_2d[0][0] = 1;
        array_2d[0][1] = 2;
        array_2d[0][2] = 3;

        array_2d[1][0] = 1;
        array_2d[1][1] = 3;
        array_2d[1][2] = 5;

        array_2d[2][0] = 2;
        array_2d[2][1] = 4;
        array_2d[2][2] = 6;
        System.out.println(array_2d);


    }
}
